#include <unistd.h>

void maff_alpha(void)
{
int a = 'a';
int b = 'B';

while(b <= 'Z')
	{
		write(1,&a,1);
		write(1,&b,1);
		a = a+2;
		b = b+2;
	}
		write(1,"\n",1);
}

int main()
{
	maff_alpha();
	return(0);
}
