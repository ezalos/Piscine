int		ft_any(char **tab, int(*f)(char*))
{
	int i;
	int o;

	i = 0;
	o = 0;
	while (tab[i] != 0)
		o += f(tab[i++]);

	if (o == 0)
		return (0);
	else
		return (1);
}
